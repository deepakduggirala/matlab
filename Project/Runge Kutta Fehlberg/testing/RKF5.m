function [y1,y2] = RKF5(t,y,h)
    k1 = f(t,y);
    k2 = h*f(t+h/4,y+k1/4);
    k3 = h*f(t+3*h/8,y+(1/32)*(3*k1 + 9*k2));
    k4 = h*f(t+12*h/13,y+(1/2197)*(1932*k1 -7200*k2 + 7296*k3));
    k5 = h*f(t+h,y+(439/216)*k1-8*k2+(3680/513)*k3 - (845/4104)*k4);
    k6 = h*f(t+h/2,y-(8/27)*k1 + 2*k2 - (3544/2565)*k3 + (1859/4104)*k4 -(11/40)*k5);
    
    y1 = y + (25/216)*k1 + (1408/2565)*k3 + (2197/4101)*k4 -(1/5)*k5;
    y2 = y + (16/135)*k1 + (6656/12825)*k3 + (28561/56430)*k4 -(9/50)*k5 + (2/55)*k6;
    abs(y2-y1);
end